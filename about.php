                                <p align="justify">
CINEMA 21 dibawah naungan PT. Nusantara Sejahtera Raya merupakan kelompok bioskop terbesar di Indonesia yang memulai kiprahnya di industri hiburan sejak tahun 1987. Lebih dari 32 tahun, CINEMA 21 berkomitmen untuk senantiasa memberikan pengalaman dan kenikmatan nonton terbaik untuk masyarakat Indonesia. Sampai dengan Januari tahun ini, CINEMA 21 memiliki total 1182 layar tersebar di 52 kota di 218 lokasi di seluruh Indonesia. 
 <br><br>
Selain menyajikan film-film hasil karya anak bangsa, CINEMA 21 juga menayangkan film-film berkelas dunia.  
 <br><br>
CINEMA 21 terus mengikuti perkembangan teknologi dengan melengkapi fasilitas-fasilitasnya seperti 2D dan 3D. Di tahun 2012, CINEMA 21 telah menghadirkan pengalaman nonton dengan teknologi revolusioner yaitu IMAX teater. Kini sudah ada tujuh teater IMAX yang dimiliki oleh CINEMA 21, yang berlokasi di Mal Gandaria City, Mal Kelapa Gading, Summarecon Mal Serpong, Summarecon Mal Bekasi, Tunjungan 5 XXI, Pakuwon Mall XXI, dan yang paling terbaru ada di The Breeze XXI BSD, Tangerang.
 <br><br>
Untuk menyempurnakan pelayanan kepada penonton, telah hadir juga bioskop dengan system audio mutakhir "Dolby Atmos" yang kini ada di 59 layar Cinema XXI.
 <br><br>
Segala informasi, data, atau material apapun yang ada dalam website ini adalah hak milik dari PT. Nusantara Sejahtera Raya (selanjutnya disebut 21Cineplex) dan milik pemegang hak resmi atas merek, logo, material yang bersangkutan.</p>
